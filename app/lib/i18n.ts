export type TranslationKey = keyof typeof translations.en;

export const getLocalizedText = (value: LocalizedText, lang: Language) =>
  value[lang];

export const formatCurrency = (
  value: number,
  lang: Language,
  fractionDigits?: number
) => {
  const formatted =
    typeof fractionDigits === "number" ? value.toFixed(fractionDigits) : `${value}`;
  return lang === "ar"
    ? `${formatted} ${translations[lang].currency}`
    : `${translations[lang].currency} ${formatted}`;
};
